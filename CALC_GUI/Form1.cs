        private void button1_Click(object sender, EventArgs e)
        {
            decimal num1 = numericUpDown1.Value;
            decimal num2 = numericUpDown2.Value;
            string op = comboBox1.SelectedItem.ToString();
            decimal result = 0;

            switch (op)
            {
                case "+":
                    result = num1 + num2;
                    break;
                case "-":
                    result = num1 - num2;
                    break;
                case "*":
                    result = num1 * num2;
                    break;
                case "/":
                    result = num1 / num2;
                    break;
            }

            label1.Text = result.ToString();
        }

